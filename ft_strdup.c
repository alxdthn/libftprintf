#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(s1);
	if (!(dup = (char *)ft_strnew(sizeof(char) * len)))
		return (NULL);
	if (len == 0)
	{
		dup[i] = '\0';
		return (dup);
	}
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
