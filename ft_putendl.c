#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;

	if (!s || !(*s))
	{
		ft_putchar('\n');
		return ;
	}
	i = 0;
	while (s[i])
		ft_putchar(s[i++]);
	ft_putchar('\n');
}
